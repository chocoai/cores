package com.lanen.model.schedule;

import java.io.Serializable;

public class TblScheduleTempReader implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -1264826871071831427L;
	private String id;
	private String tempID;
	private int readerType;
	private String reader;
	private int WriteFinishFlag;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTempID() {
		return tempID;
	}
	public void setTempID(String tempID) {
		this.tempID = tempID;
	}
	public int getReaderType() {
		return readerType;
	}
	public void setReaderType(int readerType) {
		this.readerType = readerType;
	}
	public String getReader() {
		return reader;
	}
	public void setReader(String reader) {
		this.reader = reader;
	}
	public int getWriteFinishFlag() {
		return WriteFinishFlag;
	}
	public void setWriteFinishFlag(int writeFinishFlag) {
		WriteFinishFlag = writeFinishFlag;
	}
	
	

}
