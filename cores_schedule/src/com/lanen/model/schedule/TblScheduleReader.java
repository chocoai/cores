package com.lanen.model.schedule;

import java.io.Serializable;

public class TblScheduleReader implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 745685485739297230L;
	
	private String id;
	private String scheduleID;
	private int readerType;
	private String reader;
	private int WriteFinishFlag;
	
	
	public String getScheduleID() {
		return scheduleID;
	}
	public void setScheduleID(String scheduleID) {
		this.scheduleID = scheduleID;
	}
	public int getReaderType() {
		return readerType;
	}
	public void setReaderType(int readerType) {
		this.readerType = readerType;
	}
	public String getReader() {
		return reader;
	}
	public void setReader(String reader) {
		this.reader = reader;
	}
	public int getWriteFinishFlag() {
		return WriteFinishFlag;
	}
	public void setWriteFinishFlag(int writeFinishFlag) {
		WriteFinishFlag = writeFinishFlag;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	

}
