package com.lanen.model.path;



/**
 * 芯片阅读器
 * @author Administrator
 *
 */
public class TblChipReader implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4797989772619121197L;
	
	private String chipCode;			//芯片阅读器编号            	varchar(60)    
	private String chipModel ;		//芯片阅读器型号            	varchar(100)    
	private String chipName  ;		//芯片阅读器名称            	varchar(200)    
	private int baud ;				//波特率                	integer     
	private int dataBit  ;			//数据位            	integer         
	private int stopBit  ;			//停止位            	integer         
	private int parit  ;			//校验位       		integer       
	public String getChipCode() {
		return chipCode;
	}
	public void setChipCode(String chipCode) {
		this.chipCode = chipCode;
	}
	public String getChipModel() {
		return chipModel;
	}
	public void setChipModel(String chipModel) {
		this.chipModel = chipModel;
	}
	public String getChipName() {
		return chipName;
	}
	public void setChipName(String chipName) {
		this.chipName = chipName;
	}
	public int getBaud() {
		return baud;
	}
	public void setBaud(int baud) {
		this.baud = baud;
	}
	public int getDataBit() {
		return dataBit;
	}
	public void setDataBit(int dataBit) {
		this.dataBit = dataBit;
	}
	public int getStopBit() {
		return stopBit;
	}
	public void setStopBit(int stopBit) {
		this.stopBit = stopBit;
	}
	public int getParit() {
		return parit;
	}
	public void setParit(int parit) {
		this.parit = parit;
	}
	
	

}
