package com.lanen.model.path;

import java.util.Date;

/**
 * 天平校准索引表
 * @author 黄国刚
 *
 */
public class TblBalCalibrationIndex implements java.io.Serializable{

	private static final long serialVersionUID = -7024442781892052254L;
	
	private String id;         //20
	private String hostCode;   //计算机编号20
	private String balCode;    //天平编号20
	private Date calBeginTime; //校准开始时间
	private Date calEndTime;   //校准结束时间
	private int calResult;     //校准结果  		0未通过；1通过
	private String calSign;    //校准人签字20
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHostCode() {
		return hostCode;
	}
	public void setHostCode(String hostCode) {
		this.hostCode = hostCode;
	}
	public String getBalCode() {
		return balCode;
	}
	public void setBalCode(String balCode) {
		this.balCode = balCode;
	}
	public Date getCalBeginTime() {
		return calBeginTime;
	}
	public void setCalBeginTime(Date calBeginTime) {
		this.calBeginTime = calBeginTime;
	}
	public Date getCalEndTime() {
		return calEndTime;
	}
	public void setCalEndTime(Date calEndTime) {
		this.calEndTime = calEndTime;
	}
	public int getCalResult() {
		return calResult;
	}
	public void setCalResult(int calResult) {
		this.calResult = calResult;
	}
	public String getCalSign() {
		return calSign;
	}
	public void setCalSign(String calSign) {
		this.calSign = calSign;
	}
	
	

}
