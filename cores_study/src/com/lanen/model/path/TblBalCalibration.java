package com.lanen.model.path;

import java.util.Date;

/**
 * 天平校准记录
 * @author 黄国刚
 *
 */
public class TblBalCalibration implements java.io.Serializable {

	private static final long serialVersionUID = -576270117329468234L;
	
	private String id;              // 20
	private String calIndexId;      //校准索引id 20
	private String cpCode;          //砝码编号 20
	private String cpWeight;        //砝码重量 20
	private String balValue;        //天平读数 20
	private String tolerance;       //偏差 20
	private String toleranceUpper;  //允差上限 20
	private String toleranceLower;  //允差下限 20
	private Date calTime;           //校准时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCalIndexId() {
		return calIndexId;
	}
	public void setCalIndexId(String calIndexId) {
		this.calIndexId = calIndexId;
	}
	public String getCpCode() {
		return cpCode;
	}
	public void setCpCode(String cpCode) {
		this.cpCode = cpCode;
	}
	public String getCpWeight() {
		return cpWeight;
	}
	public void setCpWeight(String cpWeight) {
		this.cpWeight = cpWeight;
	}
	public String getBalValue() {
		return balValue;
	}
	public void setBalValue(String balValue) {
		this.balValue = balValue;
	}
	public String getTolerance() {
		return tolerance;
	}
	public void setTolerance(String tolerance) {
		this.tolerance = tolerance;
	}
	public String getToleranceUpper() {
		return toleranceUpper;
	}
	public void setToleranceUpper(String toleranceUpper) {
		this.toleranceUpper = toleranceUpper;
	}
	public String getToleranceLower() {
		return toleranceLower;
	}
	public void setToleranceLower(String toleranceLower) {
		this.toleranceLower = toleranceLower;
	}
	public Date getCalTime() {
		return calTime;
	}
	public void setCalTime(Date calTime) {
		this.calTime = calTime;
	}
	
	

}
