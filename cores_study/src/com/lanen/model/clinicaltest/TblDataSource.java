package com.lanen.model.clinicaltest;

import java.io.Serializable;

/**
 * 数据源信息
 * @author Administrator
 *
 */
public class TblDataSource  implements Serializable  {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1346053178088099367L;
	private String dsId;                //数据源ID
	private String dsHost;              //设备-计算机
	private String dsInstrumentId;      //设备-仪器ID
	private String dsInstrumentName;    //设备-仪器名称
	private String dsSoftware;          //软件名称
	private String dsVersion;           //版本
	public String getDsId() {
		return dsId;
	}
	public void setDsId(String dsId) {
		this.dsId = dsId;
	}
	public String getDsHost() {
		return dsHost;
	}
	public void setDsHost(String dsHost) {
		this.dsHost = dsHost;
	}
	public String getDsInstrumentId() {
		return dsInstrumentId;
	}
	public void setDsInstrumentId(String dsInstrumentId) {
		this.dsInstrumentId = dsInstrumentId;
	}
	public String getDsInstrumentName() {
		return dsInstrumentName;
	}
	public void setDsInstrumentName(String dsInstrumentName) {
		this.dsInstrumentName = dsInstrumentName;
	}
	public String getDsSoftware() {
		return dsSoftware;
	}
	public void setDsSoftware(String dsSoftware) {
		this.dsSoftware = dsSoftware;
	}
	public String getDsVersion() {
		return dsVersion;
	}
	public void setDsVersion(String dsVersion) {
		this.dsVersion = dsVersion;
	}
	
}
