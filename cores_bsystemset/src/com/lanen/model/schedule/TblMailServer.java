package com.lanen.model.schedule;

/**
 * 邮件附件地址及账号信息
 * @author Administrator
 *
 */
public class TblMailServer implements java.io.Serializable {

	private static final long serialVersionUID = -4897132314596425500L;
	
	private String id;				// id = 1;
	private String smtpServer;		//邮件服务器地址
	private String mailCode;		//邮件账号
	private String password;		//密码
	private int isAuto;				//是否自动发送
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSmtpServer() {
		return smtpServer;
	}
	public void setSmtpServer(String smtpServer) {
		this.smtpServer = smtpServer;
	}
	public String getMailCode() {
		return mailCode;
	}
	public void setMailCode(String mailCode) {
		this.mailCode = mailCode;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getIsAuto() {
		return isAuto;
	}
	public void setIsAuto(int isAuto) {
		this.isAuto = isAuto;
	}
	
	
	

}
