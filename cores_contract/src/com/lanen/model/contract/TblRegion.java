package com.lanen.model.contract;

import java.io.Serializable;

/**
 * 地区表
 * @author Administrator
 *
 */
public class TblRegion implements Serializable {

	private static final long serialVersionUID = -3375942704229250489L;

	private String id;			
	private String regionName;		// 名称
	private String pid;				// 层级
	
	private int level;      		// 1:国家   2：省 3：市
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
}
