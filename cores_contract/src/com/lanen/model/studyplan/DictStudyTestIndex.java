package com.lanen.model.studyplan;

import java.io.Serializable;

/**
 * 字典-课题缺省检验指标
 * @author Administrator
 *
 */
public class DictStudyTestIndex   implements Serializable  {
	                                                         
	/**
	 * 
	 */
	private static final long serialVersionUID = 7010356468313300615L;
	private Long id;                                          //主键，自增长
	private DictStudyType dictStudyType =new DictStudyType();//课题类别  
	private int testItem;                                    //检验项目    1：生化  2：血常规 3：血凝  4：尿常规
	private String indexName;                                 //检验指标名称  
	private String indexAbbr;                                //检验指标缩写
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public DictStudyType getDictStudyType() {
		return dictStudyType;
	}
	public void setDictStudyType(DictStudyType dictStudyType) {
		this.dictStudyType = dictStudyType;
	}
	public int getTestItem() {
		return testItem;
	}
	public void setTestItem(int testItem) {
		this.testItem = testItem;
	}
	public String getIndexName() {
		return indexName;
	}
	public void setIndexName(String indexName) {
		this.indexName = indexName;
	}
	public String getIndexAbbr() {
		return indexAbbr;
	}
	public void setIndexAbbr(String indexAbbr) {
		this.indexAbbr = indexAbbr;
	}

	
}
